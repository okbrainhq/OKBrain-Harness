/**
 * Build document context prompt
 */
export function buildDocumentContextPrompt(documents: Array<{ title: string; content: string }>): string {
  const contextParts = documents.map(doc => `Context from document "${doc.title}":\n\n${doc.content}`);
  return contextParts.join('\n\n---\n\n') + `\n\nWarning: The above content is from internal documents specific to this conversation. Answer the user's questions based on this context if relevant.`;
}

/**
 * Build user facts context prompt (auto-extracted from conversations)
 */
export function buildFactsContextPrompt(facts: Array<{ category: string; fact: string }>): string {
  const grouped: Record<string, string[]> = {};
  for (const f of facts) {
    if (!grouped[f.category]) grouped[f.category] = [];
    grouped[f.category].push(f.fact);
  }

  let text = 'USER FACTS (Auto-extracted from conversations):\n';
  for (const [category, items] of Object.entries(grouped)) {
    text += `\n[${category}]\n`;
    for (const item of items) {
      text += `- ${item}\n`;
    }
  }
  text += '\n(These facts were auto-extracted from past conversations. Use them for context, but if they conflict with USER MEMORY above, prefer USER MEMORY as it is more accurate and curated.)';
  return text;
}

/**
 * Build recent conversations context prompt
 */
export function buildRecentConversationsPrompt(
  conversations: Array<{ title: string; userMessages: string[] }>
): string {
  let text = 'RECENT CONVERSATIONS (Last few conversations for context):\n';
  for (const conv of conversations) {
    text += `\n[${conv.title}]\n`;
    for (const msg of conv.userMessages) {
      text += `- ${msg}\n`;
    }
  }
  text += '\n(Use for context continuity, but don\'t proactively reference unless the user brings up a related topic.)';
  return text;
}

/**
 * Build RAG facts context prompt (semantically matched facts for the current message)
 */
export function buildRagFactsContextPrompt(
  facts: Array<{ fact: string; category: string; distance: number; last_extracted_at: string | null }>
): string {
  let text = 'POSSIBLY RELATED FACTS (Semantic search results based on the current message):\n\n';
  for (const f of facts) {
    const timeInfo = f.last_extracted_at
      ? ` (last extracted: ${f.last_extracted_at})`
      : '';
    text += `- [${f.category}] ${f.fact}${timeInfo}\n`;
  }
  text += '\nIMPORTANT: These facts were found via semantic text matching and could be completely unrelated to the current question — do NOT assume relevance. Only use a fact if it clearly and directly applies. Also consider the extraction time — older facts may be outdated.';
  return text;
}

/**
 * Build acknowledgement message for context injection
 */
export function buildContextAcknowledgement(contextType: 'events' | 'timezone' | 'memory' | 'facts' | 'ragFacts' | 'recentConversations' | 'documents'): string {
  switch (contextType) {
    case 'events':
      return "I have noted your upcoming events and will convert times to your local timezone.";
    case 'timezone':
      return "I understand the timezone handling requirements.";
    case 'memory':
      return "I have noted your personal details and preferences.";
    case 'facts':
      return "I have noted the user facts and will use them for context.";
    case 'ragFacts':
      return "I have noted the possibly related facts and will consider them if relevant.";
    case 'recentConversations':
      return "I have noted the recent conversation topics.";
    case 'documents':
      return "I understand. I will use the provided document context to answer your questions.";
  }
}
