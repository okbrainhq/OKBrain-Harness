/**
 * Build time context string
 * Uses UTC time for consistency and cacheability
 */
export function buildTimeContext(location?: string): string {
  const now = new Date();
  let timeContext = `Current UTC time: ${now.toISOString()}`;

  if (location) {
    timeContext += `\nUser Location (lat,lng): ${location}`;
  }

  return timeContext;
}

/**
 * Build generic grounding instructions
 * Models should customize this based on their specific search capabilities
 */
export function buildGroundingInstructions(
  isFirstMessage: boolean,
  searchToolName: string = "search"
): string {
  return isFirstMessage
    ? `\n\nIMPORTANT: Use ${searchToolName} to ensure real-world facts are accurate and up-to-date. Provide inline citations for factual information. For creative or subjective parts, citations are not required. NEVER include a 'Citations' list at the bottom.`
    : `\n\nIMPORTANT: Use ${searchToolName} primarily for fact-checking real-world details. If you state facts, verify them and provide inline citations. For creative tasks or conversation, use it only when needing current information. NEVER include a 'Citations' list at the bottom.`;
}

